var modal = document.querySelector(".modal");
var triggers = document.querySelectorAll(".trigger");

function toggleModal(sic) {
    const student = students.find(student => student.sic === sic);
    if (student) {
        document.querySelector("#circle_name").textContent = student.first_name +" • " + student.first_name +" • " + student.first_name +" • " + student.first_name +" • " + student.first_name  ;
        document.querySelector(".modal img").setAttribute("src", "https://erp.silicon.ac.in/uploads/student_photo/SITBBS_MCA_2022-2024/MCA/"+student.sic +"/"+student.sic+"_128X128.jpg");
        document.querySelector(".modal .main_content h5").textContent = student.first_name;
        document.querySelector(".modal .main_content h4").textContent = student.name;
        document.querySelector(".modal .main_content a").href = `mailto:${student.email}`;
    } else {
        document.querySelector("#circle_name").textContent = "names • names • names • names • names";
        document.querySelector(".modal img").setAttribute("src", "/assets/noImage.webp");
        document.querySelector(".modal .main_content h5").textContent = "no name";
        document.querySelector(".modal .main_content h4").textContent = "no name";
        document.querySelector(".modal .main_content a").href = "#";
    }
    modal.classList.toggle("show-modal");
}

function windowOnClick(event) {
    if (event.target === modal) {
        toggleModal();
    }
}
window.addEventListener("click", windowOnClick);